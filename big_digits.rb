LETTER_WIDTH = 5

DIGITS = ['-**----*--***--***---*---****--**--****--**---**--',
          '*--*--**-----*----*-*--*-*----*-------*-*--*-*--*-',
          '*--*---*---**---**--****-***--***----*---**---***-',
          '*--*---*--*-------*----*----*-*--*--*---*--*----*-',
          '-**---***-****-***-----*-***---**---*----**---**--',
          '--------------------------------------------------']

ARGF.each_line do |line|
  DIGITS.each_with_index do |_, i|
    line.gsub(/\D/, '').each_char do |j|
      print DIGITS[i][j.to_i * LETTER_WIDTH, LETTER_WIDTH]
    end
    puts
  end
end
